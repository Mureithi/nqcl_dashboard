CREATE TABLE `refsubs` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `name` varchar(30) DEFAULT NULL,
  `source` varchar(30) DEFAULT NULL,
  `batch_no` varchar(30) DEFAULT NULL,
  `rs_code` varchar(30) DEFAULT NULL,
  `date_received` date DEFAULT NULL,
  `effective_date` date DEFAULT NULL,
  `date_of_expiry` date DEFAULT NULL,
  `date_of_restandardisation` date DEFAULT NULL,
  `potency` decimal(10,6) NOT NULL,
  `potency_unit` varchar(15) NOT NULL,
  `init_mass` decimal(6,2) NOT NULL,
  `init_mass_unit` varchar(15) NOT NULL,
  `status` varchar(30) NOT NULL,
  `restandardisation_status` varchar(20) NOT NULL,
  `application` varchar(30) NOT NULL,
  `standard_type` varchar(30) NOT NULL,
  `version_id` int(11) NOT NULL,
  `created_at` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `updated_at` timestamp NOT NULL DEFAULT '0000-00-00 00:00:00',
  `comment` varchar(255) NOT NULL DEFAULT 'No comment',
  `edit_status` int(11) NOT NULL DEFAULT '0',
  `quantity` int(11) NOT NULL,
  `rs_code_prefix` varchar(30) NOT NULL,
  `serial_no` varchar(30) NOT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1