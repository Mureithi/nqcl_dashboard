CREATE TABLE `clients_log` (
  `log_id` int(11) NOT NULL AUTO_INCREMENT,
  `clients_log_id` int(11) DEFAULT NULL,
  `old_clients_log_name` varchar(100) DEFAULT NULL,
  `new_clients_log_name` varchar(100) DEFAULT NULL,
  `old_clients_log_alias` varchar(100) DEFAULT NULL,
  `new_clients_log_alias` varchar(100) DEFAULT NULL,
  `old_clients_log_address` varchar(100) DEFAULT NULL,
  `new_clients_log_address` varchar(100) DEFAULT NULL,
  `old_clients_log_client_type` varchar(5) DEFAULT NULL,
  `new_clients_log_client_type` varchar(5) DEFAULT NULL,
  `old_clients_log_contact_person` varchar(25) DEFAULT NULL,
  `new_clients_log_contact_person` varchar(25) DEFAULT NULL,
  `old_clients_log_contact_phone` varchar(10) DEFAULT NULL,
  `new_clients_log_contact_phone` varchar(10) DEFAULT NULL,
  `old_clients_log_version_id` int(11) DEFAULT NULL,
  `new_clients_log_version_id` int(11) DEFAULT NULL,
  `old_clients_log_created_at` datetime DEFAULT NULL,
  `new_clients_log_created_at` datetime DEFAULT NULL,
  `old_clients_log_updated_at` datetime DEFAULT NULL,
  `new_clients_log_updated_at` datetime DEFAULT NULL,
  `old_clients_log_clientid` int(11) DEFAULT NULL,
  `new_clients_log_clientid` int(11) DEFAULT NULL,
  `old_clients_log_comment` varchar(255) NOT NULL DEFAULT 'No Comment',
  `new_clients_log_comment` varchar(255) NOT NULL DEFAULT 'No Comment',
  `log_date` datetime DEFAULT NULL,
  `action` varchar(10) DEFAULT NULL,
  `who` varchar(25) DEFAULT NULL,
  `new_clients_log_client_status` int(11) NOT NULL,
  `old_clients_log_client_status` int(11) NOT NULL,
  PRIMARY KEY (`log_id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1