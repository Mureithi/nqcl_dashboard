  `old_user_log_lname` varchar(25) NOT NULL,
  `new_user_log_lname` varchar(255) DEFAULT NULL,
  `old_user_log_email` varchar(255) DEFAULT NULL,
  `new_user_log_email` varchar(255) DEFAULT NULL,
  `old_user_log_username` varchar(255) DEFAULT NULL,
